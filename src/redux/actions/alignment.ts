export const setHorizontalAlign = (align: string) => ({
  type: "SET_HORIZONTAL_ALIGN",
  payload: align,
});

export const setVerticalAlign = (align: boolean) => ({
  type: "SET_VERTICAL_ALIGN",
  payload: align,
});

export const setVerticalPosition = (align: boolean) => ({
  type: "SET_VERTICAL_POSITION",
  payload: align,
});
